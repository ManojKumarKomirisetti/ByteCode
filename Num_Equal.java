//Write a program to check if the two numbers 23 and 45 are equal.
class Num_Equal
{
    public static void main(String args[])
    {
        int a = 23, b = 45;
        if(a == b)
        {
            System.out.println("Both are equal!");
        }
        else
        {
            System.out.println("Both are not Equal !!");
        }
    }
}